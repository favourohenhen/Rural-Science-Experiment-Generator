// downloadExperiment.ts
// Generates a plain-text summary of an experiment and triggers a browser download.
// No libraries required — uses the native Blob + anchor-click technique.

import type { FlatExperiment } from '../types'

// ── Format helpers ────────────────────────────────────────────────────────────

function bulletList(items?: string[]): string {
  if (!items || items.length === 0) return '  (none listed)'
  return items.map((item) => `  • ${item}`).join('\n')
}

function numberedList(items?: string[]): string {
  if (!items || items.length === 0) return '  (none listed)'
  return items.map((item, i) => `  ${i + 1}. ${item}`).join('\n')
}

function divider(char = '─', length = 50): string {
  return char.repeat(length)
}

// ── Main export ───────────────────────────────────────────────────────────────

export function downloadExperiment(experiment: FlatExperiment): void {
  const {
    title,
    subject_name,
    topic_name,
    short_description,
    materials,
    local_alternatives,
    instructions,
    safety_notes,
    concept_explanation,
    quiz,
  } = experiment

  const lines: string[] = []

  lines.push(divider('═'))
  lines.push(`EXPERIMENT: ${title ?? 'Untitled'}`)
  lines.push(divider('═'))
  lines.push('')
  lines.push(`Subject : ${subject_name ?? '—'}`)
  lines.push(`Topic   : ${topic_name ?? '—'}`)
  lines.push('')

  if (short_description) {
    lines.push(divider())
    lines.push('DESCRIPTION')
    lines.push(divider())
    lines.push(short_description)
    lines.push('')
  }

  lines.push(divider())
  lines.push('SAFETY NOTES')
  lines.push(divider())
  lines.push(bulletList(safety_notes))
  lines.push('')

  lines.push(divider())
  lines.push('MATERIALS NEEDED')
  lines.push(divider())
  lines.push(bulletList(materials))
  lines.push('')

  lines.push(divider())
  lines.push('LOCAL ALTERNATIVES')
  lines.push(divider())
  lines.push(bulletList(local_alternatives))
  lines.push('')

  lines.push(divider())
  lines.push('INSTRUCTIONS')
  lines.push(divider())
  lines.push(numberedList(instructions))
  lines.push('')

  if (concept_explanation) {
    lines.push(divider())
    lines.push('WHAT YOU ARE LEARNING')
    lines.push(divider())
    lines.push(concept_explanation)
    lines.push('')
  }

  if (quiz && quiz.length > 0) {
    lines.push(divider())
    lines.push('QUIZ QUESTIONS')
    lines.push(divider())
    quiz.forEach((q, i) => {
      lines.push('')
      lines.push(`Q${i + 1}: ${q.question}`)
      ;(q.options ?? []).forEach((opt, oi) => {
        const letter = String.fromCharCode(65 + oi)
        lines.push(`  ${letter}. ${opt}`)
      })
      lines.push(`  ✓ Answer: ${q.answer}`)
      if (q.explanation) {
        lines.push(`  💡 ${q.explanation}`)
      }
    })
    lines.push('')
  }

  lines.push(divider('═'))
  lines.push('Generated by Rural Science Experiment Generator')
  lines.push(divider('═'))

  const content = lines.join('\n')

  // Create a Blob and trigger a download via a hidden anchor
  const blob = new Blob([content], { type: 'text/plain;charset=utf-8' })
  const url  = URL.createObjectURL(blob)
  const a    = document.createElement('a')
  a.href     = url
  // Sanitise the title for use as a filename
  const safeName = (title ?? 'experiment')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
  a.download = `${safeName}.txt`
  document.body.appendChild(a)
  a.click()
  document.body.removeChild(a)
  URL.revokeObjectURL(url)
}
